// Try increment and decrement operators on a java variable
// 1. a++ : first use value and then increment
// 2. ++a : first increment and then use value
// 3. a-- : first use value and then decrement
// 4. --a : first decrement and then use value

public class quiz2 {

    public static void main(String[] args) {
        int a = 9;
        System.out.println(a);
        System.out.println(a++);
        System.out.println(a);
        System.out.println(++a);
        System.out.println(a);
        System.out.println(a--);
        System.out.println(a);
        System.out.println(--a);
        System.out.println(a);
    }
}
