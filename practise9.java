// practise set 2.4
// Write the following expression in a java program:
// (v^2-u^2)/2as

public class practise9 {

    public static void main(String[] args) {
        int u = 60;
        int v = 50;
        int a = 10;
        int s = 2;
        int result = ((u * u) - (v * v)) / (2 * a * s);
        System.out.println(result);
    }
}
